          <p>Published on <time datetime="2019-05-20"><strong>May 20, 2020</strong></time></p>
        </header>
        <h3><strong>It is more fun exercising with a friend</strong></h3>
        <p class="animate-left">It gives a great opportunity to catch up and maintain already existing relationships but also the opportunity to create new ones in the process plus the benefits of a healthier lifestyle.<br><br>Research shows that arrangements and commitments are great motivators to stick with the fitness journey. Not wanting to let your friend or workout partner down can be of great importance, but also the advantages of having each other for pep talk and support. The research also points out that people are more likely to have a mirrored fitness result in losing weight if their work out partner is performing well.<br><br>Work out partners can bring out the competitive spirit and spur you on. Instead of giving up you are triggering each other in doing just “one more set”. Also, the intensity level tends to be higher together with someone else than on your own.</p>
