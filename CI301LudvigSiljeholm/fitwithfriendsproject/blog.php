          <h3>When to eat before exercising</h3>
          <p>Published on <time datetime="2019-05-11"><strong>May 11, 2020</strong></time></p>
        </header>
        <h3><strong>The Timing of Your Pre-Workout Meal Is Key</strong></h3>
        <p class="animate-left">The timing of your meal is also an important aspect of pre-exercise nutrition.<br><br>To maximise the results of your training, try to eat a complete meal containing carbs, protein and fat 2–3 hours before you exercise.<br><br>However, in some cases, you may not be able to get in a full meal 2–3 hours before working out.<br><br>In that case, then you can still eat a decent pre-workout meal. However, keep in mind that the sooner you eat before your workout, the smaller and simpler the meal should be.<br><br>If you eat 45–60 minutes prior to your workout, choose foods that are simple to digest and contain mainly carbs and some protein.<br><br>This will help prevent any stomach discomfort during exercise.<br></p>
